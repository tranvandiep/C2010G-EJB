    private void reloadDataTable() {
        bookList = bookSessionBean.getBookList();
        
        tableModel.setRowCount(0);
        
        bookList.stream().forEach((book) -> {
            tableModel.addRow(new Object[] {
                tableModel.getRowCount() + 1,
                book.getTitle(),
                book.getPrice(),
                book.getAuthorName()
            });
        });
    }
    
