    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        String title = txtTitle.getText();
        float price = Float.parseFloat(txtPrice.getText());
        String authorName = txtAuthorName.getText();
        
        Books b;
        
        if(currentIndex >= 0) {
            b = bookList.get(currentIndex);
        } else {
            b = new Books();
            b.setCreatedAt(new Date());
        }
        b.setAuthorName(authorName);
        b.setTitle(title);
        b.setPrice(price);
        b.setUpdatedAt(new Date());
        
        if(currentIndex >= 0) {
            bookSessionBean.update(b);
        } else {
            bookSessionBean.create(b);
        }
        resetBtnActionPerformed(null);
        
        reloadDataTable();
    }//GEN-LAST:event_saveBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int index = tblStudent.getSelectedRow();
        
        if(index >= 0) {
            int option = JOptionPane.showConfirmDialog(rootPane, "BAN CHAC CHAN MUON XOA QUAN SACH NAY KHONG?");
            if(option == 0) {
                bookSessionBean.delete(bookList.get(index).getId());
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "CHON QUAN SACH CAN XOA");
        }
        resetBtnActionPerformed(null);
        
        reloadDataTable();
    }//GEN-LAST:event_deleteBtnActionPerformed

